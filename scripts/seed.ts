async function main() {
  // Dynamic imports run AFTER dotenv() has populated process.env
  const { db } = await import('../src/lib/db');
  const { users } = await import('../src/lib/db/schema');

  console.log('Seeding users table…');

  await db
    .insert(users)
    .values({ noreg: 'ADM001', name: 'QCC Administrator', role: 'ADMIN' })
    .onConflictDoNothing();

  const rows = PARTICIPANTS.map(([noreg, name]) => ({ noreg, name, role: 'PARTICIPANT' as const }));

  const BATCH = 50;
  for (let i = 0; i < rows.length; i += BATCH) {
    await db.insert(users).values(rows.slice(i, i + BATCH)).onConflictDoNothing();
    console.log(`  batch ${Math.floor(i / BATCH) + 1} inserted`);
  }

  console.log(`Done — ${rows.length} participants + 1 admin seeded.`);
  process.exit(0);
}

